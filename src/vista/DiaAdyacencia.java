    private void cargarCombos() {
        try {
            Utilidades.cargarComboPosiciones(cbxOrigen, posicionController);
            Utilidades.cargarComboPosiciones(cbxDestino, posicionController);
        } catch (Exception e) {
        }
    }

    private void cargarTabla() {
        mta.setGnde(posicionController.getGrafo());
        tblAdyacencia.setModel(mta);
        mta.fireTableStructureChanged();
        tblAdyacencia.updateUI();
    }
    
    private void fijarAdyacencia(){
        Integer inicio = cbxOrigen.getSelectedIndex();
        Integer destino = cbxDestino.getSelectedIndex();
        if(inicio == destino){
            JOptionPane.showMessageDialog(null, "No se debe escoger la misma posici√≥n", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                posicionController.getGrafo().
                        insertarArista(
                                (inicio + 1), 
                                (destino + 1), 
                                posicionController.calcularDistancia((inicio +1), (destino +1)));
                cargarTabla();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

